// Получить случайное слово из списка ответов
export function getRandomWord(): string {
  const words = ANSWER_WORDS.filter(w => [...w].length === 5);
  return words[Math.floor(Math.random() * words.length)];
}

// Получить слово по дате (одно слово в день)
export function getDailyWord(): string {
  const words = ANSWER_WORDS.filter(w => [...w].length === 5);
  const start = new Date(2024, 0, 1);
  const now = new Date();
  const diff = Math.floor((now.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
  return words[diff % words.length];
}

// Проверить, является ли слово допустимым для ввода
export function isValidWord(word: string): boolean {
  const lowerWord = word.toLowerCase();
  return ANSWER_WORDS.some(w => w === lowerWord) || VALID_GUESSES.some(w => w === lowerWord);
}
